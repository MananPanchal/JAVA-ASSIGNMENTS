package com.phoenix.math;

// Business Logic Class - Model
public class Calculator {
	
	// Business Logic Method - Model Method
	public int add(int a, int b) {
		return a + b;
	}
	
	// Model Method
	public int divide(int n, int d) {
		return n/d;
	}

}
